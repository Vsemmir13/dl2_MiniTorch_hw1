class Variable(Protocol):
    def accumulate_derivative(self, x: Any) -> None:
        pass

    @property
    def unique_id(self) -> int:
        pass

    def is_leaf(self) -> bool:
        pass

    def is_constant(self) -> bool:
        pass

    @property
    def parents(self) -> Iterable["Variable"]:
        pass

    def chain_rule(self, d_output: Any) -> Iterable[Tuple["Variable", Any]]:
        pass


def topological_sort(variable: Variable) -> Iterable[Variable]:
    """
    Computes the topological order of the computation graph.

    Args:
        variable: The right-most variable

    Returns:
        Non-constant Variables in topological order starting from the right.
    """
    checked_node = set()
    reverse_sort = list()
    def topological_helper(node: Variable):
        if node.unique_id in checked_node or node.is_constant():
            return
        checked_node.add(node.unique_id)
        for input_node in node.parents:
            topological_helper(input_node)
        reverse_sort.append(node)
    topological_helper(variable)
    return reversed(reverse_sort)


def backpropagate(variable: Variable, deriv: Any) -> None:
    """
    Runs backpropagation on the computation graph in order to
    compute derivatives for the leave nodes.

    Args:
        variable: The right-most variable
        deriv  : Its derivative that we want to propagate backward to the leaves.

    No return. Should write to its results to the derivative values of each leaf through `accumulate_derivative`.
    """
    grads = {variable.unique_id: deriv}
    for node in topological_sort(variable):
        if node.is_constant():
            continue
        dnode = grads.get(node.unique_id, 0.0)
        if node.is_leaf():
            node.accumulate_derivative(dnode)
            continue
        for input_node, grad in node.chain_rule(dnode):
            if input_node.is_constant():
                continue
            grads[input_node.unique_id] = grads.get(input_node.unique_id, 0.0) + grad


@dataclass
class Context:
    """
    Context class is used by `Function` to store information during the forward pass.
    """

    no_grad: bool = False
    saved_values: Tuple[Any, ...] = ()

    def save_for_backward(self, *values: Any) -> None:
        "Store the given `values` if they need to be used during backpropagation."
        if self.no_grad:
            return
        self.saved_values = values

    @property
    def saved_tensors(self) -> Tuple[Any, ...]:
        return self.saved_values
